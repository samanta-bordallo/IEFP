#include <iostream>
using namespace std;
/*
neste programa vamos aprender a trabalhar com variaveis
*/
int main() {
    int x=15;
    x=10;
    cout << x <<endl;//endline

    int numi = 5;               // Integer (qualquer numero sem parte decimal)
    double numd = 5.99;    // Floating point number (com parte decimal)
    char letrac = 'D';         // Character
    string textos = "Hello";     // String (texto)
    bool booleano = false;       // Boolean (verdadeiro ou falso)

    cout << numi << " " << endl;    //endl serve para mudar de linha
    cout << numd << endl;
    cout << letrac << endl;
    cout << textos << endl;
    cout << booleano << endl;    
}
